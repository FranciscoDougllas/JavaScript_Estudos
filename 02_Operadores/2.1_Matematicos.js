//Operadores matemáticos

// Adição
let soma = 10 + 5;
console.log(soma); // 15

// Subtração
let subtracao = 10 - 5;
console.log(subtracao); // 5

// Multiplicação
let multiplicacao = 10 * 5;
console.log(multiplicacao); // 50

// Divisão
let divisao = 10 / 5;
console.log(divisao); // 2

// Módulo
let modulo = 10 % 3;
console.log(modulo); // 1

// Exponenciação
let potencia = 2 ** 3;
console.log(potencia); // 8 (2 elevado a 3)


