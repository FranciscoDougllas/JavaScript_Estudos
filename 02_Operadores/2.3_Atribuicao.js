//Operadores de atribuição

// Atribuição simples
let x = 10;
console.log(x); // 10

// Atribuição com adição
let y = 5;
y += 3;
console.log(y); // 8 (5 + 3)

// Atribuição com subtração
let z = 10;
z -= 2;
console.log(z); // 8 (10 - 2)

// Atribuição com multiplicação
let w = 2;
w *= 3;
console.log(w); // 6 (2 * 3)

// Atribuição com divisão
let v = 10;
v /= 2;
console.log(v); // 5 (10 / 2)

// Atribuição com módulo
let u = 10;
u %= 3;
console.log(u); // 1 (10 % 3)

// Atribuição com exponenciação
let t = 2;
t **= 3;
console.log(t); // 8 (2 elevado a 3)

// Atribuição com concatenação
let s = "Olá";
s += " mundo!";
console.log(s); // "Olá mundo!" (concatenação de strings)

// Atribuição com desestruturação
let [a, b] = [1, 2];
console.log(a, b); // 1 2 (desestruturação de arrays)

// Atribuição com desestruturação de objetos
let { c, d } = { c: 3, d: 4 };
console.log(c, d); // 3 4 (desestruturação de objetos)




